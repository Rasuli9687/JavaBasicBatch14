package com.syntax.class01;

public class PrimitiveDateType {

	public static void main(String[] args) {

		// Storing and representing numeric values;
// we use the below primitive for the whole numbers.
		byte box1 = 20;
		short box2 = 100;
		int box3 = 10000; // popular
		long box4 = 10000000000l; // credit cards, transaction,

		System.out.println(box1);
		System.out.println(box2);
		System.out.println(box3);
		System.out.println(box4);

		// storing and representing decimal values;
		// at the end of float values f to represent float.
		float variable1 = 1.99f;
		double variable2 = 99.99; // popular

		System.out.println(variable1);
		System.out.println(variable2);

		// storing single character

		char container = 'A';
		char dollar = '$';

		System.out.println(container);// printing value of the variable.
		System.out.println("container");// printing the text.
		System.out.println(dollar);

		// storing yes or no values.
		boolean hot = true;
		boolean tired = false;

		System.out.println(tired);
		System.out.println(hot);
		System.out.println(true);

	}

}
